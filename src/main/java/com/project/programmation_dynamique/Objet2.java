package com.project.programmation_dynamique;

public class Objet2 {
    private float poids;

    private float valeur;

    public Objet2() {
    }

    public Objet2(float poids, float valeur) {
        this.poids = poids;
        this.valeur = valeur;
    }

    public float getPoids() {
        return poids;
    }

    public void setPoids(float poids) {
        this.poids = poids;
    }

    public float getValeur() {
        return valeur;
    }

    public void setValeur(float valeur) {
        this.valeur = valeur;
    }
}
